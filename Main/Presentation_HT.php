            Notre approche<br>

            L’horticulture et les espaces paysagers sont abordés sous deux angles complémentaires au sein du Campus,
            avec l’appui du ministère de l’agriculture et de l’alimentation et l’apport d’un partenaire essentiel :
            l’Ecole Nationale Supérieure du paysage. (ENSP).<br><br>

            Intégrée à une approche filière, à travers les enjeux des matières végétales dans la rénovation du bâti, la
