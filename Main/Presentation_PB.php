            Notre approche

            Les formations du patrimoine bâti de notre réseau rassemblent les filières de la construction et de la
            rénovation du bâti avec une dimension « monument historique ». Nous accompagnons les établissements et les
            apprenants dans le développement de parcours de formations innovants en favorisant la pédagogie par le
            projet.<br><br>



            Le comité filière patrimoine bâti<br>

            Le Comité Filière réunit des professionnels et organisations qui contribuent à réfléchir à l’avenir des
            filières et à imaginer les formations en lien avec leurs besoins. Il réunit des établissements du
            secondaire, du supérieur, des laboratoires de recherche, des entreprises, des associations, des
            collectivités et des organisations professionnelles, qui, ensemble, élaborent les propositions du Campus sur
