            Notre approche<br><br>

            Au travers d’une dynamique de projets variés et innovants, nous valorisons ces métiers et les formations qui
            y conduisent, avec la contribution de partenaires prestigieux, du monde culturel et économique, au premier
            rang desquels, le château de Versailles terrain incontestable de pratique pédagogique sur l’ensemble des
            métiers d’art. Nous multiplions les rencontres avec des artisans de renom à travers des workshops, des
            visites d’ateliers ou des co-expositions. Nous ouvrons les horizons vers des métiers rares à forte valeur
            ajoutée et en tension et accompagnons chacun pour trouver sa voie.<br><br>



            Le Comité Filière Métiers d’art et design<br><br>

            Le Comité Filière réunit des professionnels et organisations qui contribuent à réfléchir à l’avenir des
            filières et à imaginer les formations en lien avec leurs besoins. Il réunit des établissements du
            secondaire, du supérieur, des laboratoires de recherche, des entreprises, des associations, des
            collectivités et des organisations professionnelles, qui, ensemble, élaborent les propositions du Campus sur
            ce thème.<br><br>

