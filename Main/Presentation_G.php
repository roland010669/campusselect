            La culture « du bien manger » est à la fois un art de vivre à la française et une « institution » reconnue
            dans le monde.<br><br>

            Inscrite au Patrimoine Mondial Immatériel de l’UNESCO, Patrimoine immatériel de l’humanité, la gastronomie
            offre une myriade de professions différentes permettant des perspectives variées : métiers de la cuisine, du
            service et de l’hébergement, de bouche et d’accueil. Aujourd’hui en forte tension, elle s’inscrit dans une
            évolution associant traditions et innovations. Elle se réinvente pour notre plus grand plaisir.<br><br>

            L’adaptation des apprentissages aux enjeux écologiques, aux nouveaux procédés et aux attentes nouvelles des
            consommateurs ouvre encore le champ de la créativité et des parcours professionnels.<br><br>



            Notre approche<br><br>

            Les formations proposées ou labellisées par Campus permettent aux apprenants de développer les compétences
            sur :<br><br>

            les enjeux des différents systèmes agricoles et l’agriculture durable
            les ressources terrestres et marines
            les enjeux d’approvisionnement
            les variétés locales anciennes d’aliments, de saveurs…
            la notion de terroirs
            la conception d’assiettes nouvelles, réduites en produits d’origine animale, plus riches en protéines
            végétales
            la compréhension des patrimoines culinaires et la création de traditions à l’aune des enjeux aujourd’hui
            les enjeux de la traçabilité des aliments
            l’agencement d’espaces de restauration au regard des enjeux énergétiques, écologiques et des attentes
            nouvelles des consommateurs
            l’influence des traditions pour impulser des innovations
            les enjeux de marketing et de tourisme culinaires
            le développement d’initiatives et le sens de l’entrepreneuriat
            Le Comité Filière Gastronomie<br><br>

            Le Comité Filière réunit des professionnels et organisations qui contribuent à réfléchir à l’avenir des
            filières et à imaginer les formations en lien avec leurs besoins. Il réunit des établissements du
            secondaire, du supérieur, des laboratoires de recherche, des entreprises, des associations, des
